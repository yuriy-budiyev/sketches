package com.github.yuriybudiyev.sketches.main.imageloader.di

import android.content.Context
import android.os.Build
import coil.ImageLoader
import coil.decode.GifDecoder
import coil.decode.ImageDecoderDecoder
import coil.decode.SvgDecoder
import coil.decode.VideoFrameDecoder
import com.github.yuriybudiyev.sketches.main.imageloader.executor.ImageLoaderExecutor
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.android.qualifiers.ApplicationContext
import dagger.hilt.components.SingletonComponent
import kotlinx.coroutines.asCoroutineDispatcher
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
object ImageLoaderModule {

    @Provides
    @Singleton
    fun provideImageLoader(
        @ApplicationContext
        context: Context,
    ): ImageLoader {
        val dispatcher = ImageLoaderExecutor().asCoroutineDispatcher()
        return ImageLoader
            .Builder(context)
            .components {
                if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.P) {
                    add(ImageDecoderDecoder.Factory())
                } else {
                    add(GifDecoder.Factory())
                }
                add(SvgDecoder.Factory())
                add(VideoFrameDecoder.Factory())
            }
            .fetcherDispatcher(dispatcher)
            .decoderDispatcher(dispatcher)
            .transformationDispatcher(dispatcher)
            .interceptorDispatcher(dispatcher)
            .build()
    }
}
